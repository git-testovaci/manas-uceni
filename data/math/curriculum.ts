type LessonInput = {
  order: number;
  title: string;
  description: string;
  area: MathCurriculumArea;
  topics: MathTopic[];
  difficultyProfile: MathDifficultyProfile;
  preset?: MathLessonPreset;
};

function buildGradeLessons(
  grade: SchoolGrade,
  lessons: LessonInput[],
): MathLesson[] {
  return lessons.map((lesson) => ({
    id: `math-g${grade}-${String(lesson.order).padStart(2, "0")}`,
    grade,
    order: lesson.order,
    title: lesson.title,
    description: lesson.description,
    area: lesson.area,
    topics: lesson.topics,
    difficultyProfile: lesson.difficultyProfile,
    preset: lesson.preset,
  }));
}

